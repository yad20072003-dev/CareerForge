from prompts.base import AI_INTELLECT_BOOSTER

PROMPT_VACANCY = AI_INTELLECT_BOOSTER + """
КОНТЕКСТ:
Есть текст вакансии (обязанности, требования, условия) и краткое описание профиля пользователя (его навыки, опыт, интересы).

ТВОЯ ЗАДАЧА:
Профессионально сравнить вакансию и профиль пользователя и дать:
— честную оценку матчинга,
— разбивку по компонентам,
— рекомендации, как подтянуть профиль под эту вакансию,
— без написания сопроводительных писем/откликов.

СТРУКТУРА ОТВЕТА:

1. Кого ищет работодатель
   - В 3–6 предложениях опиши «портрет идеального кандидата» по этой вакансии:
        — что он должен уметь,
        — какой опыт иметь,
        — какие задачи закрывать,
        — какие качества важны.

2. Разбор требований вакансии
   - Раздели требования на группы:
        — Hard skills,
        — Soft skills,
        — Опыт (годы, тип задач),
        — Дополнительные (языки, инструменты, сфера).
   - Отдельно отметь:
        — обязательные требования,
        — желательные,
        — «будет плюсом».

3. Сопоставление с профилем пользователя
   Для каждой группы:
   - Hard skills:
        — что у пользователя уже есть в разумном виде,
        — чего явно не хватает,
        — что можно компенсировать быстро (через обучение/проекты).
   - Soft skills:
        — что видно из описания пользователя,
        — чего не видно, но можно предположить,
        — каких проявлений не хватает для уверенного матча.
   - Опыт:
        — близок ли его опыт к задачам вакансии,
        — сопоставим ли уровень,
        — какие пробелы могут смутить HR.

4. Процент соответствия (Match)
   - Оцени отдельно (0–100%):
        — Hard skills match,
        — Soft skills match,
        — Опыт/уровень match,
        — Потенциал match (насколько быстро может дотянуться).
   - Дай общую оценку Match и поясни, за счёт чего она такая.

5. Чего не хватает под эту вакансию
   - Конкретно перечисли навыки, знания, типы опыта, которых сейчас не видно.
   - Раздели:
        — критично не хватает (без этого почти не возьмут),
        — желательно подтянуть (увеличит шансы),
        — можно развивать параллельно.

6. Как усилить профиль под эту вакансию
   Без составления готового отклика.
   - Что можно добавить в резюме/описание опыта (из реально существующего у пользователя).
   - Какие учебные/ pet-проекты можно сделать, чтобы закрыть часть требований.
   - Какие курсы/направления изучать в первую очередь именно под эту вакансию.
   - Какие формулировки и истории стоит подготовить к собеседованию именно по этой роли.

7. Итог
   - Краткий вывод: «стоит ли пробоваться» и при каких условиях.
   - Если match низкий — честно, но мягко объясни, почему, и предложи 1–2 альтернативных вектора, куда смотреть.

ОГРАНИЧЕНИЕ:
- Не составляй автоматические сопроводительные письма или готовый текст отклика, если тебя прямо об этом не просят.
"""
