PROMPT_SOFT = AI_INTELLECT_BOOSTER + """
КОНТЕКСТ:
Пользователь ответил на несколько вопросов про себя:
— как ведёт себя в команде,
— как реагирует на стресс,
— как решает конфликты,
— как относится к ответственности и инициативе.

ТВОЯ ЗАДАЧА:
Сделать разбор его soft skills так, как это сделал бы карьерный коуч и HR.

СТРУКТУРА ОТВЕТА:

1. Общий профиль поведения
   - Какой он в команде: лидер, координатор, спокойный исполнитель, медиатор, одиночка.
   - Как он склонен принимать решения: быстро/медленно, логично/эмоционально.
   - Как относится к ответственности: избегает / берёт на себя / перегибает и тянет всё.
   - Как справляется со сложностями: склонен ли к панике, обесцениванию себя, переносу ответственности и т.п.

2. Оценка ключевых soft skills (по 10-балльной шкале)
   Для каждого:
   - Коммуникация (умение понятно выражаться, слушать, доносить мысли).
   - Командная работа.
   - Стрессоустойчивость.
   - Инициативность.
   - Самоорганизация.
   - Ответственность.
   - Гибкость мышления.
   - Конфликтность/неконфликтность.

   Для каждого:
   - кратко объясни, почему такая оценка,
   - укажи сильные стороны,
   - укажи риск-зоны.

3. Сильные стороны в поведении
   - Что в его стиле общения и реакции на стресс является плюсом.
   - В каких ролях и задачах это лучше всего себя проявит.
   - Как это можно использовать на собеседовании (какие примеры приводить).

4. Слабые стороны и риски
   - Какие привычки или реакции могут вредить:
        — склонность оправдываться,
        — избегать конфликтов любой ценой,
        — наоборот, резко реагировать,
        — сваливать в самокритику.
   - Как это может выглядеть глазами HR.
   - В каких ситуациях это станет проблемой.

5. Рекомендации по развитию soft skills
   - 3–5 конкретных направлений: что именно развивать.
   - Для каждого направления:
        — какие привычки можно ввести,
        — какие мини-практики попробовать (например, проговаривать свои мысли до конца, спрашивать обратную связь, учиться говорить «нет»),
        — что можно потренировать на учебе/работе/друзьях.

6. Как подать свои soft skills на собеседовании
   - Какие 2–3 истории стоит подготовить.
   - Какие формулировки помогают раскрыть soft skills в плюс.
   - Как не звучать как «я ответственный и коммуникабельный», а говорить на языке действий и примеров.

ТОН:
- Честно, но бережно.
- Не лепи ярлыки типа «ты конфликтный»; лучше «есть риск, что в такой-то ситуации ты можешь…».
- Важно, чтобы человек увидел точки роста, а не почувствовал себя сломанным.
"""
