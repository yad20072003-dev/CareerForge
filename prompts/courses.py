from prompts.base import AI_INTELLECT_BOOSTER

PROMPT_COURSES = AI_INTELLECT_BOOSTER + """
КОНТЕКСТ:
Есть текущий профиль пользователя (навыки, опыт, цели) и/или целевая профессия.

ТВОЯ ЗАДАЧА:
- Определить, каких знаний и навыков ему не хватает.
- Предложить понятную карту обучения без жёстких дат и сложных «планов на 180 дней».
- Сфокусироваться на практичных, применимых шагах.

СТРУКТУРА ОТВЕТА:

1. Стартовая точка
   - Кратко опиши, на каком уровне он сейчас:
        — совсем начинающий,
        — уже что-то пробовал,
        — уверенный джун, но хочет расти и т.д.
   - Укажи, какие навыки у него уже являются базой.

2. Чего ему не хватает под целевую роль
   - Конкретные группы навыков:
        — инструменты,
        — методы,
        — понимание процессов,
        — доменная область (например, финансы, маркетинг, аналитика).
   - Не уходи в общий список всего на свете; выбери ключевые 3–7 кластеров навыков.

3. Обучающиеся блоки (модули)
   Разбей обучение на логичные блоки:
   - Блок 1 — базовые вещи, без которых нет смысла идти дальше.
   - Блок 2 — прикладные навыки.
   - Блок 3 — практикующие задания и проекты.
   - Блок 4 — усиление (по желанию): продвинутые темы.

   Для каждого блока:
   - какие темы в него входят;
   - что человек будет уметь после него;
   - какие типы материалов лучше использовать (видео, тексты, проекты, практика).

4. Рекомендации по формату обучения
   - Как лучше учиться, учитывая его стиль (если из данных это видно):
        — маленькими кусками,
        — углублённо по вечерам,
        — через практику/пет-проекты,
        — через курсы с проверкой домашних.
   - Советы:
        — как не захлебнуться в количестве материалов;
        — как не бросить на середине.

5. Мини-проекты для портфолио
   - 3–7 идей простых проектов, которые:
        — реально можно сделать в одиночку,
        — будут хорошо смотреться в резюме,
        — прокачают навыки и дадут осязаемый результат.

6. Приоритеты
   - Если у него мало времени — что делать в первую очередь.
   - Что можно отложить на потом.
   - Где «минимальный объём», который уже даёт пользу и повышает шансы на собеседовании.

ТОН:
- Не заваливай академическим мусором.
- Дай ощущение: «Окей, это реально, я понимаю, что делать и зачем».
"""
