from prompts.base import AI_INTELLECT_BOOSTER

PROMPT_RESCHECK = AI_INTELLECT_BOOSTER + """
КОНТЕКСТ:
Тебе дали резюме и целевую должность.

ЗАДАЧА:
Сделать глубокий, честный, но поддерживающий разбор резюме с точки зрения:
- HR, который нанимает на эту роль,
- ATS-сканера,
- здравого смысла.

СТРУКТУРА АНАЛИЗА:

1. Общее впечатление
   - Как выглядит кандидат по резюме в целом.
   - Что думает о нём типичный HR после первого прочтения.
   - Насколько понятно, кто он и на что претендует.

2. Сильные стороны резюме
   - Какие блоки уже хорошо оформлены.
   - Какие формулировки удачные и сильные.
   - Где видно конкретику, результаты, ясную структуру.

3. Слабые стороны и красные флаги
   Разбери:
   - Структура: нет ли хаоса, повторов, логических провалов.
   - Содержание: слишком общие фразы, отсутствие результатов, много «воды».
   - Формальные штуки: много ли текста, тяжело ли читать, кривые формулировки.
   - Красные флаги:
        — частая смена места работы,
        — большие провалы по времени,
        — отсутствие описания обязанностей/результатов,
        — несостыковки между опытом и целевой должностью,
        — «слишком всего и сразу» без фокуса.
   Объясни, почему это может смутить HR.

4. Соответствие целевой должности
   - Насколько текущая версия резюме «попадает» в запрос целевой роли.
   - Какие навыки/опыт видно сразу.
   - Чего не видно, хотя возможно оно есть (и это проблема подачи).
   - На что HR, скорее всего, обратит внимание в первую очередь.

5. Конкретные рекомендации по улучшению
   Дай не абстрактные советы, а конкретные действия:
   - «убрать такие-то фразы…»
   - «заменить формулировку X на Y»
   - «добавить 2–3 пункта про результаты там-то»
   - «укоротить/переписать блок об обязанностях»
   - «вынести такие-то навыки в отдельный блок»

6. Улучшенная версия резюме (черновой вариант)
   - Перепиши резюме так, чтобы:
        — оно лучше соответствовало целевой должности,
        — стало более структурированным,
        — убралась «вода»,
        — появились результаты и понятные обязанности.
   - Не придумывай новые места работы и опыт — работай только с тем, что дал пользователь, но усиливай формулировки.

7. Финальный вывод
   - Оцени резюме по 10-балльной шкале «до» и «после» предполагаемых правок.
   - Кратко укажи, какой блок даст максимальный прирост качества, если его доработать.

ТОН:
- Профессиональный, но человеческий.
- Без высокомерия, без обесценивания.
- С акцентом: «это всё поправимо, вот как именно».
"""
