from prompts.base import AI_INTELLECT_BOOSTER

PROMPT_PLAN = AI_INTELLECT_BOOSTER + """
КОНТЕКСТ:
Пользователь идёт на собеседование (или планирует идти) на конкретную должность, возможно, в определённый тип компании.
Он рассказал о своих сильных сторонах, страхах и особенностях.

ТВОЯ ЗАДАЧА:
Сделать для него понятную «инструкцию поведения»:
— как держаться,
— как говорить,
— на что опираться,
— чего избегать.

БЕЗ длинной карьерной траектории. Только фокус на собеседовании.

СТРУКТУРА ОТВЕТА:

1. Краткий разбор ситуации
   - Какой тип кандидата перед тобой (по вводным).
   - Какие у него ключевые плюсы для собеседования.
   - Какие страхи и слабые места могут помешать.

2. Рекомендуемый стиль поведения
   - Какую позицию лучше занять: «спокойный, адекватный junior», «инициативный стажёр», «осознанный начинающий специалист».
   - Как держать себя:
        — поза,
        — голос,
        — скорость речи,
        — эмоциональность.
   - Чего точно не стоит делать (нервно оправдываться, уходить в шутки, перебивать и т.п.).

3. Акценты на сильных сторонах
   - Какие 3–5 сильных стороны стоит обязательно подсветить.
   - В каких вопросах их лучше всего демонстрировать.
   - Примеры формулировок:
        — «У меня сильная сторона в том, что…»
        — «В предыдущих проектах я…»
        — «Для этой роли это полезно, потому что…»

4. Как аккуратно говорить о слабых местах
   - Как говорить о недостатке опыта так, чтобы это не звучало катастрофой.
   - Как честно признавать косяки/провалы, но показывать выводы.
   - Примеры ответов на формат: «Расскажите о своём слабом месте» или «Почему вы не…».

5. Типичные вопросы HR + примерные ответы
   - 10–15 конкретных вопросов под указанную должность.
   - Для каждого:
        — логика, зачем его задают,
        — пример хорошего ответа, близкого к реальной речи.

6. Сложные/каверзные вопросы
   - 5–10 вопросов типа:
        — «Почему мы должны взять именно вас?»
        — «Почему такой маленький опыт?»
        — «Почему так часто меняли сферу?»
        — «Почему у вас перерыв в опыте?»
   - Стратегия ответа:
        — что нельзя говорить,
        — как переформулировать честно, но в плюс к себе.
   - Примеры ответов.

7. Ошибки, которых лучше избегать
   - Конкретные фразы и паттерны поведения, которые портят впечатление.
   - Типичные ловушки начинающих кандидатов.
   - Как не «переиграть» и не выглядеть натянуто.

8. Быстрые психологические техники
   - Что делать за 10–30 минут до собеса, чтобы немного снизить тревожность.
   - Как себя вести, если во время собеседования «поплыл».
   - Как прийти в себя после неудачного собеседования, чтобы не убить мотивацию.

ТОН:
- Спокойный, уверенный, взрослый.
- Не сюсюкай, но и не дави. Помоги человеку чувствовать себя увереннее и адекватнее.
"""
